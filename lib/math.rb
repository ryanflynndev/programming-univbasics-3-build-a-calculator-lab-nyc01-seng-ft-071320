def addition(num1, num2)
  add = num1 + num2
  add 
end

def subtraction(num1, num2)
  subtract = num1 - num2
  subtract
end

def division(num1, num2)
  divide = num1 / num2 
  divide
end

def multiplication(num1, num2)
  multiply = num1 * num2
  multiply
end

def modulo(num1, num2)
  modulo_num = num1 % num2
  modulo_num 
end

def square_root(num)
  square_root_num = Math.sqrt(num)
  square_root_num
end